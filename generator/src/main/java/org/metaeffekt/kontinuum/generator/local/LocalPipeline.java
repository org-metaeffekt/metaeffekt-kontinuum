package org.metaeffekt.kontinuum.generator.local;

import lombok.Getter;
import lombok.extern.slf4j.Slf4j;
import org.apache.commons.lang3.StringUtils;
import org.metaeffekt.kontinuum.generator.shared.Pipeline;
import org.metaeffekt.kontinuum.models.local.LocalConfiguration;
import org.metaeffekt.kontinuum.models.shared.PipelineConfiguration;
import org.metaeffekt.kontinuum.models.shared.PipelineConfiguration.ProjectProperties.Asset;
import org.metaeffekt.kontinuum.models.shared.ProcessorDefinitions.Processor;
import org.metaeffekt.kontinuum.models.shared.ProcessorDefinitions.ProcessorParameter;
import org.metaeffekt.kontinuum.models.shared.Stage;

import java.util.ArrayList;
import java.util.Comparator;
import java.util.List;
import java.util.Map;

/**
 * Generates a local-execution shell script from the configured pipeline.
 *
 * <p>The output is a sequence of chained {@code mvn -f <processor-pom> <goal>} invocations,
 * one per processor, ordered by stage. The script is intended to be executed on the
 * developer's machine (macOS, Linux). Windows compatibility is left open via the
 * {@link LocalConfiguration.ExecutionEnvironment} enum for future implementation.
 */
@Slf4j
public class LocalPipeline {

    /**
     * -- GETTER --
     *  Convenience accessor for the resolved processor map. Useful for tests.
     */
    @Getter
    private final Map<Asset, List<Processor>> assetProcessorsMap;

    private final StringBuilder scriptDocument = new StringBuilder();

    private final LocalConfiguration localConfiguration;

    public LocalPipeline(PipelineConfiguration pipelineConfiguration, LocalConfiguration localConfiguration) {
        this.localConfiguration = localConfiguration;
        Pipeline pipeline = new Pipeline(pipelineConfiguration, localConfiguration);
        this.assetProcessorsMap = pipeline.generatePipeline();
    }

    public String generatePipeline() {
        generateHeader();
        generateProcessorSteps();
        return scriptDocument.toString();
    }

    private void generateHeader() {
        scriptDocument.append("#!/usr/bin/env bash").append(System.lineSeparator());
        scriptDocument.append("# Auto-generated by ae-kontinuum-execution:generate-local-pipeline").append(System.lineSeparator());
        scriptDocument.append("# Do not edit manually; regenerate from the kontinuum configuration.").append(System.lineSeparator());
        scriptDocument.append("set -e").append(System.lineSeparator());
        scriptDocument.append(System.lineSeparator());
    }

    private void generateProcessorSteps() {
        List<ProcessorStep> orderedSteps = new ArrayList<>();
        for (Map.Entry<Asset, List<Processor>> entry : assetProcessorsMap.entrySet()) {
            String assetName = entry.getKey().toString();
            for (Processor processor : entry.getValue()) {
                orderedSteps.add(new ProcessorStep(processor, assetName));
            }
        }

        orderedSteps.sort(Comparator
                .comparingInt((ProcessorStep s) -> s.processor.getStage().ordinal())
                .thenComparing(s -> s.assetName)
                .thenComparing(s -> s.processor.getId()));

        for (ProcessorStep step : orderedSteps) {
            scriptDocument.append("# --- ").append(step.processor.getStage()).append(": ")
                    .append(step.processor.getId()).append(" (").append(step.assetName).append(") ----")
                    .append(System.lineSeparator());
            scriptDocument.append(generateMavenScriptBlock(step.processor));
            scriptDocument.append(System.lineSeparator());
        }
    }

    private String generateMavenScriptBlock(Processor processor) {
        StringBuilder script = new StringBuilder();
        script.append("mvn -f ")
                .append(localConfiguration.getKontinuumProcessorsDirNormalized())
                .append(processor.getPomLocation()).append(" ")
                .append(processor.getGoal()).append(" \\").append(System.lineSeparator());

        List<ProcessorParameter> nonBlankParams = processor.getParameters().stream()
                .filter(p -> StringUtils.isNotBlank(p.getValue()))
                .toList();

        for (int i = 0; i < nonBlankParams.size(); i++) {
            ProcessorParameter param = nonBlankParams.get(i);
            script.append("    -D").append(param.getKey()).append("='").append(param.getValue()).append("'");
            if (i < nonBlankParams.size() - 1) {
                script.append(" \\");
            }
            script.append(System.lineSeparator());
        }
        return script.toString();
    }

    private record ProcessorStep(Processor processor, String assetName) {}

}